// while loop

let index=0;
while (index<=10) {
   // console.log(`value of index is ${index}`);
    index = index+2;
}

let arr=['a','b','c','d','e','f'];

let i=0;
let n = arr.length;
while (i<n) {
   // console.log(`The value at ${i} index in while loop is ${arr[i]}`);
    i=i+1;
}


// DO WHILE LOOP

let score=11;
do {
    console.log(`The score is: ${score}`);
    score++;
} while (score<=10);
